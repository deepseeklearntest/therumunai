// CCMC zones (Coimbatore).
const COIMBATORE_ZONES = [
  ["East Zone", "கிழக்கு மண்டலம்"],
  ["West Zone", "மேற்கு மண்டலம்"],
  ["North Zone", "வடக்கு மண்டலம்"],
  ["South Zone", "தெற்கு மண்டலம்"],
  ["Central Zone", "மத்திய மண்டலம்"],
];

function tile(bbox, cols, rows, index) {
  const [lngMin, latMin, lngMax, latMax] = bbox;
  const lngStep = (lngMax - lngMin) / cols;
  const latStep = (latMax - latMin) / rows;
  const col = index % cols;
  const row = Math.floor(index / cols);
  const xmin = lngMin + col * lngStep;
  const ymin = latMin + row * latStep;
  return [xmin, ymin, xmin + lngStep, ymin + latStep].map((n) => n.toFixed(6));
}

function rows(city, zones) {
  const { bbox, cols, rows: gridRows } = CITIES[city];
  return zones.map(([en, ta], i) => {
    const [xmin, ymin, xmax, ymax] = tile(bbox, cols, gridRows, i);
    const en_ = en.replace(/'/g, "''");
    const ta_ = ta.replace(/'/g, "''");
    const geom = `ST_Multi(ST_SetSRID(ST_MakeEnvelope(${xmin}, ${ymin}, ${xmax}, ${ymax}), 4326))`;
    return `  ('${city}', '${en_}', '${ta_}', true, ${geom})`;
  });
}

const values = [...rows("Chennai", CHENNAI_ZONES), ...rows("Coimbatore", COIMBATORE_ZONES)];

const sql = `-- 0001_zones.sql  (GENERATED by db/generate-placeholder-zones.mjs — do not hand-edit)
-- BLOCKER-1: PLACEHOLDER polygons (is_placeholder = true). Not real ward
-- boundaries. Replace with authoritative GCC/CCMC GeoJSON before launch.
-- Tamil names are best-effort; verify with a native reader (HARD RULE 4).

INSERT INTO municipal_zones (city, zone_name, zone_name_ta, is_placeholder, geom)
VALUES
${values.join(",\n")}
ON CONFLICT (city, zone_name) DO NOTHING;
`;

const out = join(here, "seeds", "0001_zones.sql");
writeFileSync(out, sql);
console.log(`Wrote ${values.length} placeholder zones to ${out}`);
